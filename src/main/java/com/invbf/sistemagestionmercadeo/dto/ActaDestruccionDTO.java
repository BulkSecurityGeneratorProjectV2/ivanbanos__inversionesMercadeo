package com.invbf.sistemagestionmercadeo.dto;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.Date;
import java.util.List;

/**
 *
 * @author ivan
 */
public class ActaDestruccionDTO implements Serializable{
    private Integer id;
    private Date fecha;
    private String usuario;
    private String estado;
    private List<BarajasCantidad> detalle;

    public ActaDestruccionDTO() {
        detalle = new ArrayList<BarajasCantidad>();
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public List<BarajasCantidad> getDetalle() {
        return detalle;
    }

    public void setDetalle(List<BarajasCantidad> detalle) {
        this.detalle = detalle;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
}
